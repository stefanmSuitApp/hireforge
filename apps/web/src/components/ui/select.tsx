SelectSeparator.displayName = SelectPrimitive.Separator.displayName;

export type SelectFieldOption = {
  value: string;
  label: React.ReactNode;
};

export type SelectFieldClassNames = {
  trigger?: string;
  content?: string;
  item?: string;
};

export type SelectFieldProps = Omit<
  React.ComponentPropsWithoutRef<typeof SelectPrimitive.Root>,
  'children'
> & {
  options: SelectFieldOption[];
  placeholder?: string;
  /** Merged onto each slot after default styles (use for layout overrides). */
  classNames?: SelectFieldClassNames;
  id?: string;
  'aria-invalid'?: boolean;
  triggerProps?: React.ComponentPropsWithoutRef<typeof SelectPrimitive.Trigger>;
  contentProps?: React.ComponentPropsWithoutRef<typeof SelectPrimitive.Content>;
  itemProps?: React.ComponentPropsWithoutRef<typeof SelectPrimitive.Item>;
};

/**
 * Opinionated Select built from Radix primitives: options + per-slot styling and prop passthrough.
 * For full control, use `Select` + `SelectTrigger` + … exports instead.
 */
function SelectField({
  options,
  placeholder,
  classNames,
  id,
  'aria-invalid': ariaInvalid,
  triggerProps,
  contentProps,
  itemProps,
  ...rootProps
}: SelectFieldProps) {
  const {
    className: triggerClassName,
    'aria-invalid': triggerAriaInvalid,
    ...restTrigger
  } = triggerProps ?? {};
  const { className: contentClassName, ...restContent } = contentProps ?? {};
  const { className: itemClassName, ...restItem } = itemProps ?? {};

  return (
    <Select {...rootProps}>
      <SelectTrigger
        id={id}
        aria-invalid={ariaInvalid ?? triggerAriaInvalid}
        className={cn(classNames?.trigger, triggerClassName)}
        {...restTrigger}
      >
        <SelectValue placeholder={placeholder} />
      </SelectTrigger>
      <SelectContent
        className={cn(classNames?.content, contentClassName)}
        {...restContent}
      >
        {options.map((opt) => (
          <SelectItem
            key={opt.value}
            value={opt.value}
            className={cn(classNames?.item, itemClassName)}
            {...restItem}
          >
            {opt.label}
          </SelectItem>
        ))}
      </SelectContent>
    </Select>
  );
}

export { SelectField };

export {
  Select,
  SelectContent,
  SelectGroup,
  SelectItem,
  SelectLabel,
  SelectScrollDownButton,
  SelectScrollUpButton,
  SelectSeparator,
  SelectTrigger,
  SelectValue,
};
